package ejercicioestructurasdecontrol1;

public class ejercicioestructurasdecontrol1 {
	public static void main(String[] args) {
		
		int primera = 20;
		int segunda = 8;
		
		if(primera < segunda){
			System.out.println("Es menor que la primera");
			
		}
		else if(primera == segunda) {
			System.out.println("Es igual que la primera");
	}

		else if(primera > segunda) {
			System.out.println("Es mayor que la primera");
}
		else {
			System.out.println("ninguna");
		}
		
	}
}
